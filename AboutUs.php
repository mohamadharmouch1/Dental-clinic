                <h2>
                    Root Canal Treatment
                </h2>
                <p>
                    Root canal treatment (endodontics) is a dental procedure used to treat infection at the centre of a tooth.
                    Root canal treatment is not painful and can save a tooth that might otherwise have to be removed completely.
                    </br></br>
                    While all general dentists have been trained in root canals, more often than not the
                    procedure is done by an endodontist. Generally speaking, a dentist specializes in exterior teeth and gum health,
                    an endodontist specializes in the health of the inside of the tooth.</br></br>
