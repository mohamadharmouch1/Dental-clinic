                    Cosmetic Dentistry
                </h2>
                <p>
                    Cosmetic dentistry also called esthetic or aesthetic dentistry. is the branch of dentistry 
                    that focuses on improving the appearance of your smile. 
                    Common cosmetic dental treatments include teeth whitening,dental bonding and veneers.</br></br>
                    Cosmetic dental procedures can eliminate all sorts of issues, from problems related  
                    to jaw alignment to being able to speak effectively. Cosmetic procedures like bonding 
                    help maintain healthier teeth,and gum contouring can help correct an uneven gum line.</br></br>
                </p>
